public class C03Operators {
    public static void main(String[] args) {
        arithmeticOperatorsDemo();
    }

    static void arithmeticOperatorsDemo(){
        System.out.println("***************** Arithmetic Operators *****************");
        int n1=13,n2=3;

        System.out.println("n1="+n1+"  and n2="+n2);
        System.out.println("Multiplication: n1*n2="+(n1*n2));
        System.out.println("      Division: n1/n2="+(n1/n2));
        System.out.println("        Modulo: n1%n2="+(n1%n2));
        System.out.println("      Addition: n1+n2="+(n1+n2));
        System.out.println("   Subtraction: n1-n2="+(n1-n2));

        System.out.println("***************** END Arithmetic Operators *****************");
    }

    


}
